// src/lib/seoCategory.ts – Seed FAQ สำหรับหมวดสินค้า (คีย์เวิร์ดค้นหา)

/** FAQ seed สำหรับหมวดสินค้า 6–10 ข้อ (ประโยคคำถามแบบที่คนค้นหา) */
export function categoryFaqSeed(
  categorySlug: string,
  categoryName: string
): { q: string; a: string }[] {
  const cat = categoryName || categorySlug || "หมวดนี้";
  const base = [
    {
      q: `รับซื้อ${cat} ราคาเท่าไหร่?`,
      a: `ราคาขึ้นอยู่กับรุ่น/สเปค/สภาพ ส่งรูปและสเปคใน LINE @webuy เพื่อประเมินราคาตามสภาพจริง`,
    },
    {
      q: `ขาย${cat} ที่ไหนได้บ้าง?`,
      a: `นัดรับถึงที่ในพื้นที่บริการ หรือนำมาหน้าร้านได้ ดูรายการพื้นที่ได้ที่ /locations`,
    },
    {
      q: "ต้องเตรียมอะไรก่อนขาย?",
      a: "สำรองข้อมูล ออกจากระบบ (iCloud/Windows) ปิด Find My (MacBook) เตรียมอะแดปเตอร์/กล่อง/ใบเสร็จ ถ้ามี",
    },
    {
      q: "เครื่องเสีย/จอแตก รับซื้อไหม?",
      a: "รับซื้อครับ ราคาอิงตามอาการและมูลค่าชิ้นส่วน ส่งรูป/อาการใน LINE @webuy เพื่อประเมิน",
    },
    {
      q: "จ่ายเงินทันทีไหม?",
      a: "จ่ายเงินสดหรือโอนทันทีหลังตรวจสภาพเรียบร้อย",
    },
    {
      q: `รับซื้อ${cat} ทุกรุ่นไหม?`,
      a: `รับซื้อหลากหลายรุ่นและแบรนด์ ส่งรูป+สเปคใน LINE @webuy เพื่อเช็คว่ารับรุ่นที่คุณมี`,
    },
    {
      q: "ประเมินราคาใช้เวลานานไหม?",
      a: "ส่งรูปและสเปคใน LINE @webuy โดยปกติตอบกลับภายในวันทำการ",
    },
    {
      q: "วิธีขายเร็วที่สุด?",
      a: "ส่งรูปชัดเจน รุ่น/สเปค/สภาพ และอุปกรณ์ที่มีใน LINE @webuy ทีมงานจะประเมินให้เร็วที่สุด",
    },
  ];

  if (categorySlug === "notebook" || categoryName?.toLowerCase().includes("โน๊ตบุ๊ค")) {
    base.unshift({
      q: "รับซื้อโน๊ตบุ๊คทุกแบรนด์ไหม?",
      a: "รับครับ Acer Asus Lenovo HP Dell MSI Apple MacBook ทุกรุ่น ราคาอิงสภาพ ส่งรูป+สเปคใน LINE @webuy",
    });
  }

  return base.slice(0, 10);
}
